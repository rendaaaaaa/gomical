## ごみカレンダーの文字列を分解し、ごみ種別ごとに分ける
## 分析用
## 標準化のために曜日とおぼしき文字を○に、
## 月、日をあらわす数字を△に置換して
## 現れるパターンごとに何件あるかを集計している
sub gomical_split_bunseki {
    my ($s) = @_;

    # 。で分割できるように整形
    # $s =~ s/燃やせるごみは、/燃やせるごみは/g;
    # $s =~ s/、容器包装プラスチックは/。容器包装プラスチックは/g;
    for (@{$conf_bunseki->{normalize}}) {
        $s =~ s/$_->{re}/$_->{rep}/g;
    }

    my @sp = split $conf_bunseki->{split_ch}, $s;
    return @sp;
}

