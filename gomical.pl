## ごみカレンダーの個別ページから、意味のある文字列を抜き取る。
sub get_cal_string_array {
    my ($url) = @_;
    my $gomical_root = Mojo::URL->new($url);
    my $dom = $ua->get($gomical_root)->res->dom;
    #my $sel = 'a[href^="#h"]';
    my $sel = 'a[id^="h"]';
    my @result = ();
    $dom->find($sel)->each(sub {
            my $str = squish decode 'utf8', $_->next->next_sibling;
            say $str;
        });
    #say dumper @result;
    return @result;
}


## トップページからごみカレンダーのURL(区ごと)を得る
sub get_ku_url {
    my ($url) = @_;
    my $gomical_root = Mojo::URL->new($url);
    my $dom = $ua->get($gomical_root)->res->dom;
    my $sel = "a:not(a[href*=index])[href*=yomiage][href^=/]";
    my @ku = ();
    $dom->find($sel)->each(sub {
            push @ku, $gomical_root->path($_->attr('href'))->to_string;
        });
    #say dumper @ku;
    return @ku;
}


## すべてのごみカレンダーURLを得る
sub get_cal_url {
    my ($url) = @_;
    my @ku = get_ku_url($url);

    my $sel = "a:not(a[href*=#]):not(a[href*=index])[href*=carender][href*=yomiage][href^=/]";
    my %chou;
    for my $u (@ku) {
        my $url = Mojo::URL->new($u);
        my $dom = $ua->get($url)->res->dom;
        $dom->find($sel)->each(sub {
                my $chou_url = $url->path($_->attr('href'))->to_string;
                $chou{$chou_url} ||= 0;
                $chou{$chou_url}++;
            });
    }
    #say dumper sort keys %chou;
    return sort keys %chou;
}

