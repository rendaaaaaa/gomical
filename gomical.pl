sub gomical_parse {
    my ($gomical_str) = @_;
    say "----- 1 -----";
    say $gomical_str;
    #TODO: $conf行き
    my @h = qw/shushu moeru bin plas kami moenai eda/;
    my @sp = gomical_split($gomical_str);
    my $caldata = {};
    for (0 .. (scalar @sp)-1) {
        say "処理対象:";
        say $sp[$_];
        my $name = $h[$_];
        my @actions = grep {$name eq $_->{name}} @{$conf->{rule}};
        my $act = shift @actions;
        next unless exists $act->{method};
        say "モード: $act->{method}";
        if ($act->{method} eq 'daily') {
            say "日付取得モード";
            my ($erayear, $month) = $sp[$_] =~ /平成(\d+)年(\d+)月の/;
            $caldata->{year} = $erayear + 1989;
            $caldata->{month} = $month;
        } elsif ($act->{method} eq 'weekly') {
            say "週次モード";
            my @wday = $sp[$_] =~ m{
                (?: #グループ化対象外
                    (.)曜
                )+
            }gx;
            say join ",", @wday;
        } elsif ($act->{method} eq 'monthly') {
            say "月次モード";
            my @days = $sp[$_] =~ m{
                (?: #グループ化対象外
                \d+
                )+
            }gx;
            say join ",", @days;
        }
    }
    say Dump($caldata);
}

